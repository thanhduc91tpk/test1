package com.thanhduc91tpk.test1.model;

public class Girl {
    private String mTen;
    private int mTuoi;

    public Girl(String mTen, int mTuoi) {
        this.mTen = mTen;
        this.mTuoi = mTuoi;
    }

    public Girl() {
    }

    public String getmTen() {
        return mTen;
    }

    public void setmTen(String mTen) {
        this.mTen = mTen;
    }

    public int getmTuoi() {
        return mTuoi;
    }

    public void setmTuoi(int mTuoi) {
        this.mTuoi = mTuoi;
    }
}
